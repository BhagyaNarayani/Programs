
public class Emp {
	
	public String name;
	public int age;
	public long salary;
	
	public Emp(String name, int age, long salary) {
		super();
		this.name = name;
		this.age = age;
		this.salary = salary;
	}

	public String getName() {
		return name;
	}


	public int getAge() {
		return age;
	}

	

	public long getSalary() {
		return salary;
	}

	
	
	

}
